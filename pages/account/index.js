import React from 'react';
import Head from "next/head";
import Header from "../../components/Header";
import UserDetailComponent from "../../components/userDetailComponent/UserDetailComponent";
import {getToken} from "next-auth/jwt";

export async function getServerSideProps(context) {
    const secret = 'sdfkjhsdf92w5y29whg9240p34909yh09v0324703hn09eu2019';
    const token = await getToken({
        req: context.req,
        secret: secret
    });

    if (!token){
        return {
            redirect:{
                destination:'/',
                permanent:false
            }
        }
    }else{
        const response = await fetch(`http://localhost:8080/api/v1/auth-detail/user`,{headers:{
                Authorization: "Bearer "+token.accessToken
            }});
        const data = await response.json();

        return {
            props: {
                username:token.username,
                authorization:token.accessToken,
                token:token,
                data:data
            }
        }
    }
}

